import { promises as fs } from 'fs';
import path from 'path';
import os from 'os';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

/**
 * Ensures android/local.properties contains sdk.dir
 * - Prefers ANDROID_SDK_ROOT or ANDROID_HOME
 * - Falls back to default Windows SDK location
 */
async function ensureSdk() {
  async function exists(p) {
    try {
      await fs.access(p);
      return true;
    } catch {
      return false;
    }
  }

  function normalizeForGradle(p) {
    return p.replace(/\\/g, "\\\\");
  }

  const root = path.resolve(__dirname, "..");
  const androidDir = path.join(root, "android");
  const localPropsPath = path.join(androidDir, "local.properties");

  if (!(await exists(androidDir))) {
    console.error("android-ensure-sdk: android/ folder not found. Run `npx cap add android` first.");
    process.exit(1);
  }

  const envSdk =
    (process.env.ANDROID_SDK_ROOT && process.env.ANDROID_SDK_ROOT.trim()) ||
    (process.env.ANDROID_HOME && process.env.ANDROID_HOME.trim()) ||
    "";

  const candidates = [];
  if (envSdk) candidates.push(envSdk);

  if (process.platform === "win32") {
    const home = os.homedir();
    candidates.push(path.join(home, "AppData", "Local", "Android", "Sdk"));
  }

  let sdkDir = null;
  for (const cand of candidates) {
    if (await exists(cand) && await exists(path.join(cand, "platform-tools"))) {
      sdkDir = cand;
      break;
    }
  }

  if (!sdkDir) {
    console.error(
      "android-ensure-sdk: Android SDK not found.\n" +
        "- Install Android Studio + SDK\n" +
        "- Set ANDROID_SDK_ROOT (recommended) or ANDROID_HOME\n" +
        "- Or ensure it exists at %USERPROFILE%\\AppData\\Local\\Android\\Sdk"
    );
    process.exit(1);
  }

  const content =
    `# Auto-generated by scripts/android-ensure-sdk.mjs\n` +
    `sdk.dir=${normalizeForGradle(sdkDir)}\n`;

  await fs.writeFile(localPropsPath, content, "utf8");
  console.log(`android-ensure-sdk: wrote ${path.relative(root, localPropsPath)} -> ${sdkDir}`);
}

ensureSdk().catch(console.error);
